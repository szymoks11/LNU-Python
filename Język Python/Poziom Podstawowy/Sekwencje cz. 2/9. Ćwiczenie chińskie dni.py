def chineeseWeekDays(chineeseDays, day):
    if day == "monday":
        return chineeseDays[0]
    if day == "tuesday":
        return chineeseDays[1]
    if day == "wednesday":
        return chineeseDays[2]
    if day == "thursday":
        return chineeseDays[3]
    if day == "friday":
        return chineeseDays[4]
    if day == "saturday":
        return chineeseDays[5]
    if day == "sunday":
        return chineeseDays[6]